export function drawClock(time: string) {
  const result = [];
  const digitMap = new Map();
  digitMap.set(':', [[' '], [' '], ['*'], [' '], ['*'], [' '], [' ']]);
  digitMap.set('0', [
    ['*', '*', '*'],
    ['*', ' ', '*'],
    ['*', ' ', '*'],
    ['*', ' ', '*'],
    ['*', ' ', '*'],
    ['*', ' ', '*'],
    ['*', '*', '*'],
  ]);
  digitMap.set('1', [
    [' ', ' ', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
  ]);
  digitMap.set('2', [
    ['*', '*', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
    ['*', '*', '*'],
    ['*', ' ', ' '],
    ['*', ' ', ' '],
    ['*', '*', '*'],
  ]);
  digitMap.set('3', [
    ['*', '*', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
    ['*', '*', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
    ['*', '*', '*'],
  ]);
  digitMap.set('4', [
    ['*', ' ', '*'],
    ['*', ' ', '*'],
    ['*', ' ', '*'],
    ['*', '*', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
  ]);
  digitMap.set('5', [
    ['*', '*', '*'],
    ['*', ' ', ' '],
    ['*', ' ', ' '],
    ['*', '*', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
    ['*', '*', '*'],
  ]);
  digitMap.set('6', [
    ['*', '*', '*'],
    ['*', ' ', ' '],
    ['*', ' ', ' '],
    ['*', '*', '*'],
    ['*', ' ', '*'],
    ['*', ' ', '*'],
    ['*', '*', '*'],
  ]);
  digitMap.set('7', [
    ['*', '*', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
  ]);
  digitMap.set('8', [
    ['*', '*', '*'],
    ['*', ' ', '*'],
    ['*', ' ', '*'],
    ['*', '*', '*'],
    ['*', ' ', '*'],
    ['*', ' ', '*'],
    ['*', '*', '*'],
  ]);
  digitMap.set('9', [
    ['*', '*', '*'],
    ['*', ' ', '*'],
    ['*', ' ', '*'],
    ['*', '*', '*'],
    [' ', ' ', '*'],
    [' ', ' ', '*'],
    ['*', '*', '*'],
  ]);
  const digits = time.split('');
  const hour1 = digitMap.get(digits[0]);
  const hour2 = digitMap.get(digits[1]);
  const colon = digitMap.get(digits[2]);
  const minute1 = digitMap.get(digits[3]);
  const minute2 = digitMap.get(digits[4]);

  for (let i = 0; i < 7; i++) {
    const row: string[] = [].concat(hour1[i], ' ', hour2[i], ' ', colon[i], ' ', minute1[i], ' ', minute2[i]);
    result.push(row);
  }
  return result;
}
